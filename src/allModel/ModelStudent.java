package allModel;

public class ModelStudent {

	/**
	 * @param args
	 */
	private int id;
	private String name;
	private int age;
	private int v;
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getV() {
		return v;
	}
	public void setV(int v) {
		this.v = v;
	}
	

}
